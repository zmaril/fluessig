//! The C/C++ (extern "C" ABI) template grid — one language's projection of the
//! op shapes into a stable C ABI, plus the C header and C++ RAII wrapper.
//!
//! straitjacket-allow-file:duplication — the per-language generators are
//! DELIBERATELY parallel: the (language × shape) template grid is the design
//! (see /translation.md); the truly shared pieces live in the parent module.
//!
//! Three artifacts share this module's crossing classifier so the Rust export
//! layer ([`cpp_binding`]), the C header ([`super::cpp_header::cpp_header`]) and
//! the C++ wrapper ([`super::cpp_hpp::cpp_hpp`]) agree on every signature:
//!   * `cpp_binding`  — the Rust `extern "C"` layer, compiled INTO the cdylib.
//!   * `cpp_header`   — the plain-C `.h` (opaque handles + prototypes).
//!   * `cpp_hpp`      — the C++ `.hpp` RAII wrapper (throws `fluessig::Error`).
//!
//! The full ABI is documented in the design spec that ships with this backend.

use crate::api::{ApiDoc, ApiOp, ApiType, Shape};

use super::*;

// The C header and C++ wrapper are generated by the sibling modules; re-export
// them here so `crate::bindgen::{cpp_binding, cpp_header, cpp_hpp}` all resolve
// through `cpp::` (one re-export line in `mod.rs`, additive alongside the other
// backends).
pub use super::cpp_header::cpp_header;
pub use super::cpp_hpp::cpp_hpp;

/// This backend's language slug — the key it reads out of every symbol's
/// `bindings` map via the shared [`pinned_name`] resolver. C has no namespacing,
/// so an op pin replaces the op segment of the flat `<Iface>_<op>` symbol.
pub(super) const LANG: &str = "cpp";

// ── name helpers (shared across the three artifacts) ─────────────────────────

/// The C enum type name for a real (int-backed) enum: `Flavor` → `FlFlavor`.
pub(super) fn c_enum_name(name: &str) -> String {
    format!("Fl{}", pascal(name))
}

/// The C struct type name for a DTO model: `Chunk` → `FlChunk`.
pub(super) fn c_model_name(name: &str) -> String {
    format!("Fl{}", pascal(name))
}

/// The opaque cursor handle type name for a stream op: `Svc` + `watch` →
/// `SvcWatchStream`.
pub(super) fn c_stream_name(iface: &str, op: &ApiOp) -> String {
    format!("{iface}{}Stream", pascal(&op.name))
}

/// The per-list struct type name for an element token: `String` → `FlStringList`.
pub(super) fn c_list_name(elem_token: &str) -> String {
    format!("Fl{elem_token}List")
}

/// The op's flat C symbol: `<Iface>_<op>`, honoring an op-name pin (which
/// replaces the op segment).
pub(super) fn op_symbol(iface: &str, op: &ApiOp) -> String {
    let opname = pinned_name(&op.bindings, LANG).unwrap_or_else(|| snake(&op.name));
    format!("{iface}_{opname}")
}

/// A field's C member name, honoring a field pin.
pub(super) fn field_name(f: &crate::api::ApiField) -> String {
    pinned_name(&f.bindings, LANG).unwrap_or_else(|| snake(&f.name))
}

// ── the crossing classifier ──────────────────────────────────────────────────

/// How a return/param/field type crosses the C ABI. Shared by all three
/// artifacts so the Rust extern signature, the C prototype, and the C++ wrapper
/// agree by construction.
#[derive(Clone)]
pub(super) enum Cross {
    Void,
    I32,
    I64,
    U8,
    U16,
    U32,
    F32,
    F64,
    Bool,
    /// UTF-8 string (also the `Json` / date-time / unknown-scalar carrier).
    Str,
    /// Bulk bytes (`FlBytes` OUT, `const uint8_t*,size_t` IN).
    Bytes,
    /// A real int-backed enum (Rust ident carried for `from_c`/`as i32`).
    Enum(String),
    /// A value-carrying enum projected as a string (mirrors `is_string_enum`).
    StrEnum,
    /// A DTO model (`Fl<Model>` C struct).
    Model(String),
    /// A tagged union — crosses as its JSON envelope text (a `Str` carrier).
    Union,
    List(Box<Cross>),
    Nullable(Box<Cross>),
}

/// Classify an [`ApiType`] into its C-ABI [`Cross`].
pub(super) fn classify(api: &ApiDoc, t: &ApiType) -> Cross {
    match t {
        ApiType::Scalar(s) => match s.as_str() {
            "void" => Cross::Void,
            "int32" => Cross::I32,
            "int64" => Cross::I64,
            "uint8" => Cross::U8,
            "uint16" => Cross::U16,
            "uint32" => Cross::U32,
            "float32" => Cross::F32,
            "float64" | "float" => Cross::F64,
            "boolean" => Cross::Bool,
            "bytes" => Cross::Bytes,
            // `string`/`Json` and every other scalar (utcDateTime, ArrowBatch,
            // …) ride the UTF-8 string carrier — the honest default for a scalar
            // the C ABI has no dedicated representation for.
            _ => Cross::Str,
        },
        ApiType::Model { model } => Cross::Model(model.clone()),
        ApiType::Enum { r#enum } => {
            if is_string_enum(api, r#enum) {
                Cross::StrEnum
            } else {
                Cross::Enum(r#enum.clone())
            }
        }
        ApiType::List { list } => Cross::List(Box::new(classify(api, list))),
        ApiType::Nullable { nullable } => Cross::Nullable(Box::new(classify(api, nullable))),
        ApiType::Union { .. } => Cross::Union,
        // A truly-foreign type has no dedicated C-ABI representation; it rides the
        // UTF-8 string carrier (the honest default the scalar catchall also uses).
        // The typed opaque handle is a rust-core concern (see `rust_core`).
        ApiType::Foreign { .. } => Cross::Str,
        // cpp callback lowering is a follow-up (see notes/callback-function-types.md);
        // no committed fixture feeds cpp a callback, so the string carrier is a
        // reachable-only-in-future placeholder that keeps `classify` total.
        ApiType::Callback { .. } => Cross::Str,
    }
}

/// Classify a param, folding an `optional` marker into a [`Cross::Nullable`] so
/// the core call receives an `Option`, matching the shared [`param_sig`].
pub(super) fn classify_param(api: &ApiDoc, p: &crate::api::ApiParam) -> Cross {
    let c = classify(api, &p.ty);
    if p.optional == Some(true) {
        Cross::Nullable(Box::new(c))
    } else {
        c
    }
}

/// The element token used in list type names (`FlStringList`, `FlInt32List`, …).
pub(super) fn list_elem_token(c: &Cross) -> String {
    match c {
        Cross::I32 => "Int32".into(),
        Cross::I64 => "Int64".into(),
        Cross::U8 => "Uint8".into(),
        Cross::U16 => "Uint16".into(),
        Cross::U32 => "Uint32".into(),
        Cross::F32 => "Float32".into(),
        Cross::F64 => "Float64".into(),
        Cross::Bool => "Bool".into(),
        Cross::Str | Cross::StrEnum | Cross::Union => "String".into(),
        Cross::Bytes => "Bytes".into(),
        Cross::Enum(_) => "Int32".into(),
        Cross::Model(m) => pascal(m),
        Cross::List(inner) => format!("{}List", list_elem_token(inner)),
        Cross::Nullable(inner) => list_elem_token(inner),
        Cross::Void => "Void".into(),
    }
}

/// Collect every distinct list element crossing used anywhere in the api, keyed
/// by its list-name token (`String`, `Int32`, `FooModel`, …), in first-seen
/// order. The single source of truth for which `Fl<T>List` structs, helpers, and
/// free functions the three artifacts must agree on. Only top-level list
/// elements are visited (nested `list<list<T>>` is a documented gap — matching
/// the flat token the C header emits).
pub(super) fn collect_list_elems(api: &ApiDoc) -> Vec<(String, Cross)> {
    let mut seen = std::collections::BTreeSet::new();
    let mut out: Vec<(String, Cross)> = Vec::new();
    let mut visit = |c: &Cross, out: &mut Vec<(String, Cross)>| {
        if let Cross::List(inner) = c {
            let token = list_elem_token(inner);
            if seen.insert(token.clone()) {
                out.push((token, (**inner).clone()));
            }
        }
    };
    for m in &api.models {
        for f in &m.fields {
            visit(&classify(api, &f.ty), &mut out);
        }
    }
    for i in &api.interfaces {
        for op in &i.ops {
            visit(&classify(api, &op.returns), &mut out);
            for p in &op.params {
                visit(&classify_param(api, p), &mut out);
            }
        }
    }
    out
}

/// The core-facing Rust element type for a list element crossing (`Vec<Elem>`).
fn list_elem_rust(elem: &Cross) -> String {
    match elem {
        Cross::I32 => "i32".into(),
        Cross::I64 => "i64".into(),
        Cross::U8 => "u8".into(),
        Cross::U16 => "u16".into(),
        Cross::U32 => "u32".into(),
        Cross::F32 => "f32".into(),
        Cross::F64 => "f64".into(),
        Cross::Bool => "bool".into(),
        Cross::Str | Cross::StrEnum | Cross::Union => "String".into(),
        Cross::Bytes => "Bytes".into(),
        Cross::Enum(e) => e.clone(),
        Cross::Model(m) => m.clone(),
        // nested list / nullable elements aren't marshalled (documented gap)
        _ => "String".into(),
    }
}

/// Read one list element from a `p: *const <elem_c>` into its Rust value.
fn list_in_elem(elem: &Cross) -> String {
    match elem {
        Cross::I32
        | Cross::I64
        | Cross::U8
        | Cross::U16
        | Cross::U32
        | Cross::F32
        | Cross::F64
        | Cross::Bool => "*p".into(),
        Cross::Str | Cross::StrEnum | Cross::Union => "fl_str_in(*p)".into(),
        Cross::Enum(e) => format!("{e}::from_c(*p)"),
        Cross::Model(m) => format!("{}_from_c(&*p)", snake(m)),
        Cross::Bytes => "fl_bytes_in((*p).data, (*p).len)".into(),
        _ => "fl_str_in(*p)".into(),
    }
}

/// Move one Rust list element `x: <elem_rust>` into its owned C value `<elem_c>`.
fn list_out_elem(elem: &Cross) -> String {
    match elem {
        Cross::I32
        | Cross::I64
        | Cross::U8
        | Cross::U16
        | Cross::U32
        | Cross::F32
        | Cross::F64
        | Cross::Bool => "x".into(),
        Cross::Str | Cross::StrEnum | Cross::Union => "fl_str_out(x)".into(),
        Cross::Enum(_) => "x as i32".into(),
        Cross::Model(m) => format!("{}_to_c(x)", snake(m)),
        Cross::Bytes => "fl_bytes_out(x)".into(),
        _ => "fl_str_out(x)".into(),
    }
}

/// Free one owned C list element bound as `e: <elem_c>`; `None` when the element
/// owns no heap (plain scalars / enums cross by value).
fn list_free_elem(elem: &Cross) -> Option<String> {
    match elem {
        Cross::Str | Cross::StrEnum | Cross::Union => Some("fl_string_free(e);".into()),
        Cross::Bytes => Some("{ let mut e = e; fl_bytes_free(&mut e); }".into()),
        Cross::Model(m) => Some(format!(
            "{{ let mut e = e; fl_{}_free(&mut e); }}",
            snake(m)
        )),
        _ => None,
    }
}

/// The `#[repr(C)] Fl<T>List` struct + its in/out/free helpers, for every list
/// element the api uses. Emitted only when the api has at least one list, so a
/// list-free schema stays byte-identical to the pre-list generator.
fn emit_rust_list_support(elems: &[(String, Cross)]) -> String {
    let mut s = String::new();
    for (token, elem) in elems {
        let list_ty = c_list_name(token);
        let elem_c = rust_c_member_type(elem);
        let elem_rust = list_elem_rust(elem);
        let sn = snake(token);
        s.push_str(&format!(
            "/// The `list<{elem_rust}>` carrier across the C ABI (free with `fl_{sn}_list_free`).\n"
        ));
        s.push_str("#[repr(C)]\n");
        s.push_str(&format!(
            "pub struct {list_ty} {{\n    pub data: *mut {elem_c},\n    pub len: usize,\n}}\n\n"
        ));
        // An empty-list Default so the nullable-list write paths have a zero value.
        s.push_str(&format!(
            "impl Default for {list_ty} {{\n    fn default() -> Self {{\n        {list_ty} {{ data: std::ptr::null_mut(), len: 0 }}\n    }}\n}}\n\n"
        ));
        // in: const <elem_c>* + len → Vec<elem_rust>
        s.push_str(&format!(
            "unsafe fn {sn}_list_in(ptr: *const {elem_c}, len: usize) -> Vec<{elem_rust}> {{\n    if ptr.is_null() {{\n        return Vec::new();\n    }}\n    (0..len)\n        .map(|i| {{\n            let p = ptr.add(i);\n            {}\n        }})\n        .collect()\n}}\n\n",
            list_in_elem(elem)
        ));
        // out: Vec<elem_rust> → owned Fl<T>List
        s.push_str(&format!(
            "fn {sn}_list_out(v: Vec<{elem_rust}>) -> {list_ty} {{\n    let mut items: Vec<{elem_c}> = v.into_iter().map(|x| {}).collect();\n    items.shrink_to_fit();\n    let len = items.len();\n    let data = items.as_mut_ptr();\n    std::mem::forget(items);\n    {list_ty} {{ data, len }}\n}}\n\n",
            list_out_elem(elem)
        ));
        // free
        s.push_str(&format!(
            "#[no_mangle]\npub unsafe extern \"C\" fn fl_{sn}_list_free(p: *mut {list_ty}) {{\n    if p.is_null() {{\n        return;\n    }}\n    let p = &mut *p;\n    if !p.data.is_null() {{\n        let items = Vec::from_raw_parts(p.data, p.len, p.len);\n"
        ));
        match list_free_elem(elem) {
            Some(free) => s.push_str(&format!(
                "        for e in items {{\n            {free}\n        }}\n"
            )),
            None => s.push_str("        drop(items);\n"),
        }
        s.push_str("        p.data = std::ptr::null_mut();\n        p.len = 0;\n    }\n}\n");
    }
    s
}

