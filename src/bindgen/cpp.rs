/// The C by-value type for a scalar-ish crossing (struct members, stream item
/// type, by-value returns). Compound types return their `Fl…` name.
pub(super) fn c_value_type(c: &Cross) -> String {
    match c {
        Cross::Void => "void".into(),
        Cross::I32 => "int32_t".into(),
        Cross::I64 => "int64_t".into(),
        Cross::U8 => "uint8_t".into(),
        Cross::U16 => "uint16_t".into(),
        Cross::U32 => "uint32_t".into(),
        Cross::F32 => "float".into(),
        Cross::F64 => "double".into(),
        Cross::Bool => "bool".into(),
        Cross::Str | Cross::StrEnum | Cross::Union => "char*".into(),
        Cross::Bytes => "FlBytes".into(),
        Cross::Enum(e) => c_enum_name(e),
        Cross::Model(m) => c_model_name(m),
        Cross::List(inner) => c_list_name(&list_elem_token(inner)),
        // A struct member that is nullable rides the inner value type; the flag
        // (`has_<name>`) is emitted alongside by the member renderer.
        Cross::Nullable(inner) => c_value_type(inner),
    }
}

